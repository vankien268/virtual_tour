<?php

namespace App\Http\Controllers\CMS;

use App\Http\Controllers\Controller;
use Illuminate\Http\Request;
use Spatie\Permission\Models\Permission;
use Spatie\Permission\Models\Role;

class PermissionController extends Controller
{
    private $role;
    private $permission;
    public function __construct(Role $role, Permission $permission)
    {
        $this->role = $role;
        $this->permission = $permission;
        $this->middleware('role:Supper Admin');
    }
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $records = $this->permission->orderBy("id", "ASC")->paginate(10);
        return view('cms.Permission.index', [
            'records' => $records,
        ]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('cms.Permission.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validateStore($request);
        $data = $request->all();
        $create = $this->permission->create($data);
        if (!$create) {
            return redirect()->back()->with('error', 'Thêm mới vai trò thất bại')->withInput();
        }
        return redirect()->route('cms.manager.permissions.index')->with('success', 'Thêm mới vai trò thành công');
    }
    private function validateStore(Request $request)
    {
        $rule = ['name' => 'required'];
        $this->validate($request, $rule, [
            'name.required' => 'Tên vai trò không để trống'
        ]);
    }

